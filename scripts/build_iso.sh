#!/bin/bash
# scripts/build_iso.sh
# Phase 6 — Luminos OS ISO Builder (Arch Linux / archiso)
#
# Builds a bootable Luminos OS ISO using archiso.
# Requirements: archiso, chaotic-aur configured (for AUR packages)
#
# Usage:
#   sudo bash scripts/build_iso.sh
#
# Output: build/out/luminos-<version>-x86_64.iso

set -euo pipefail

LUMINOS_VERSION="0.1.0"
LUMINOS_CODENAME="Arch"
BUILD_DATE="$(date +%Y-%m-%d)"

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_DIR="$(dirname "$SCRIPT_DIR")"
BUILD_DIR="$REPO_DIR/build"
PROFILE_DIR="$BUILD_DIR/luminos-profile"
WORK_DIR="$BUILD_DIR/work"
OUTPUT_DIR="$BUILD_DIR/out"
LOG="$BUILD_DIR/build.log"
START_TIME="$(date +%s)"

mkdir -p "$BUILD_DIR"

log() { echo "[build_iso] $*" | tee -a "$LOG"; }
die() { log "ERROR: $*"; exit 1; }

log "=== Luminos OS ${LUMINOS_VERSION} ISO Builder ==="
log "Repo: $REPO_DIR"
log "Output: $OUTPUT_DIR/"

# ---------------------------------------------------------------------------
# Dependency check
# ---------------------------------------------------------------------------
check_deps() {
    log "--- Checking dependencies ---"
    local deps=(archiso mtools dosfstools libisoburn squashfs-tools)
    local missing=()
    for dep in "${deps[@]}"; do
        if ! pacman -Qi "$dep" &>/dev/null; then
            missing+=("$dep")
        fi
    done
    if [ "${#missing[@]}" -gt 0 ]; then
        log "Installing missing deps: ${missing[*]}"
        sudo pacman -S --noconfirm --needed "${missing[@]}"
    fi
    log "Dependencies OK"
}

